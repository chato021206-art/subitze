def inject(html, diff, svg_str):
    pattern = rf"(if\(diff==='{diff}'\) return ')(.*?)(';)"
    def replacer(m):
        return m.group(1) + svg_str + m.group(3)
    new_html = re.sub(pattern, replacer, html, flags=re.DOTALL)
    if new_html == html:
        print(f'WARNING: {diff} pattern not matched!')
    else:
        print(f'{diff}: OK')
    return new_html

